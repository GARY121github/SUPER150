package Assignment_5;

import java.util.Scanner;

/*
Given a string containing of ‘0’, ‘1’ and ‘?’ wildcard characters, generate all binary strings that can be formed by
replacing each wildcard character by ‘0’ or ‘1’.

Input Format
The first line of input contains a single integer T denoting the number of test cases. Then T test cases follow.
Each test case consist of two lines. The first line of each test case consists of a string S.

Constraints
1 ≤ T ≤ 60 1 ≤ length of string S ≤ 30

Output Format
Print all binary string that can be formed by replacing each wildcard character separated by space.

Sample Input
1
1??0?101
Sample Output
10000101 10001101 10100101 10101101 11000101 11001101 11100101 11101101
Explanation
For the Given test case, 10000101 can be generated by replacing wildcard character from 1??0?101 by 0s.
*/
public class Generate_Binary_Strings {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            String st = sc.next();
            print(st, 0);
            System.out.println();
        }
    }

    static void print(String st , int index){
        if(index == st.length()){
            System.out.print(st + " ");
            return;
        }

        if(st.charAt(index) == '?'){
            print(st.substring(0 , index) + '0' + st.substring(index+1) , index+1);
            print(st.substring(0 , index) + '1' + st.substring(index+1) , index+1);
        }
        else {
            print(st , index+1);
        }
    }

}

